										<h2 id="sta-je-ombudsman">What is the Ombudsman?</h2>
									</div>
									<div class="article-content triggerAnimation animated fontaccess" data-animate="fadeInLeft">
										<p class="fontaccess">The Ombudsman is an institution introduced in 1809 by the Swedish Parliament with an aim to controlling the executive authorities. The term “ombudsman” in Swedish language denotes a person who “has an ear for the voice of the people”. The task of the first Ombudsman was to prevent the King and Government from ignoring the laws. The first Ombudsman relied on their authority to request from administrative authorities to provide reports on the law enforcement, as well as the authority to institute certain proceedings aimed at establishing the responsibility of administrative public servants.</p>
										<p class="fontaccess">The Provincial  Protector of Citizens – Ombudsman is an autonomous and independent authority of the AP Vojvodina in charge of protecting the rights of citizens and monitoring the work of provincial administrative authorities, public enterprises and institutions with administrative and public powers, founded by the AP Vojvodina, concerning their acting in implementation of decisions and other legal acts of the AP Vojvodina.</p>
										<p class="fontaccess">Pursuant to the new Provincial Assembly Decision on the Provincial Protector of Citizens – Ombudsman, the Provincial  Protector of Citizens - Ombudsman shall have four deputies, of whom one shall be elected for the following areas: national minority rights, rights of the child and gender equality.</p>
										<p class="fontaccess">The Decision on Establishing the Institution of the Provincial Ombudsman was adopted  in December 2002.  The first Ombudsman of Vojvodina was elected on 24 September 2003 and their work commenced in January 2004. The institution is based in Novi Sad.</p>
