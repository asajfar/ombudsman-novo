										<h2 id="postupak">Proceedings of the Provincial Protector of Citizens - Ombudsman</h2>
									</div>
									<div class="article-content triggerAnimation animated fontaccess" data-animate="fadeInLeft">
										<p class="fontaccess">Following a citizen’s complaint (filed in person, via regular or electronic mail), if acting upon complaints belongs to the scope of competences of the Provincial Protector of Citizens - Ombudsman, providing the complaint was filed within the period of one year of the occurrence of violation or irregularity or enactment of the last act in the disputed matter, the Provincial Protector of Citizens - Ombudsman shall act upon that complaint.  The Provincial Protector of Citizens - Ombudsman shall inform the person who filed the complaint and the administrative authority whose work is the subject of the complaint about instituting the proceedings and request the administrative authority to respond to allegations of the complaint.</p>
										<p class="fontaccess">If requested so by the person who filed the complaint, the Ombudsman shall be obliged to keep the client’s personal data confidential.</p>
										<p class="fontaccess">During the inquiry proceedings, the Ombudsman may summon for an interview and take oral or written depositions and require additional clarifications from all persons employed with administrative authorities, the person who filed the complaint, witnesses, experts and other citizens who have some information on the circumstances of the case. The Ombudsman shall be entitled to request all data and information from the administrative authority, as well as a direct inspection of files, documents and collection of data available at these authorities, including state, official, trade and professional secrets. The Ombudsman shall have a free access to all premises in which administrative authorities carry out their activities and shall be able at any time and without prior announcement, to visit any healthcare institution providing mandatory psychiatric treatment.</p>
										<p class="fontaccess">The Ombudsman shall decide on terminating the proceedings if it is established at the end of the inquiry, that  no violation of human rights or irregular acting by authorities had occurred, or if it is found that the administrative authority whose work was the subject of the complaint, had already eliminated the violation of human rights  during the inquiry. The person who filed the complaint and administrative authority whose work was the subject of the complaint shall be notified of the termination of the proceedings.</p>
										<p class="fontaccess">Should the Ombudsman find, following the completion of the inquiry, that particular human right was violated or certain irregularity in the work occurred, the Ombudsman shall give an opinion, make a proposal or recommendation to the given administrative authority on the ways to eliminate the irregularity or its consequences. The Ombudsman shall also notify the person who filed the petition on the recommendation made.</p>
										<p class="fontaccess">The administrative authorities to which the Ombudsman has forwarded their opinion, proposal or made a recommendation, shall be obliged to notify the Ombudsman within 15 days on the measures undertaken.</p>
										<p class="fontaccess">In addition to action upon complaints of persons who claim their human rights had been violated in an act or activity of an administrative authority, the Provincial Protector of Citizens - Ombudsman may also, exceptionally, act at their own initiative, based on the provided information and other sources, should they conclude that there probably is or was a case of violation of human rights by an administrative authority.</p>
