										<h2 id="nadleznosti">Competences of the Provincial Protector of Citizens – Ombudsman</h2>
									</div>
									<div class="article-content triggerAnimation animated fontaccess" data-animate="fadeInLeft">
										<p class="fontaccess">With an aim to protect human rights, the Provincial Protector of Citizens - Ombudsman shall receive and assess the petitions pertaining to violation of human rights, supervise the enforcement of regulations, control the legality, expediency and effectiveness of actions of administrative authorities and shall investigate their work. Furthermore, the Ombudsman shall monitor the enforcement of international standards in the field of human rights, collect information about the enforcement of laws and other regulations in the field of human rights, prepare the annual report on the exercise of human rights, inform the competent authorities and wider public about the violation of human rights, initiate the instigation of criminal, disciplinary or other proceedings with competent authorities in the event of human rights violation etc.</p>
										<p class="fontaccess">The main principles of the Ombudsman’s work shall be legality, impartiality, independence and righteousness.</p>
