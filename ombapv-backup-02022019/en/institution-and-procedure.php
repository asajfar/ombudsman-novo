										<h2 id="ko-moze-da-se-obrati">Who may contact the Provincial Protector of Citizens - Ombudsman</h2>
									</div>
									<div class="article-content triggerAnimation animated fontaccess" data-animate="fadeInLeft">
										<p class="fontaccess">Any person who believes any of their human rights has been violated in an act or action by an administrative authority may contact the Ombudsman.</p>
										<p class="fontaccess">Also an heir, guardian or legal representative of the person whose right has been violated may contact the Ombudsman.</p>
										<p class="fontaccess">This could also be done by a non-governmental organisation, citizens’ association or other organisation, on behalf of the person who believes their human rights have been violated, providing a written authorisation.</p>
										<p class="fontaccess">Even persons deprived of liberty are entitled to file a complaint in a sealed envelope, whereas the employees of institutions where persons deprived of liberty are confined, are obliged to submit the complaint of the person deprived of liberty to the Ombudsman, without previously opening or retaining it.</p>
